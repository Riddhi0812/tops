"""Write a Python program to demonstrate the creation of variables and different data types. """

# 4. Variables and data types
name = "Alice"  # String
age = 25  # Integer
height = 5.8  # Float
is_student = True  # Boolean
my_list = [1, 2, 3]  # List
my_tuple = (4, 5, 6)  # Tuple
my_dict = {"name": "Bob", "city": "New York"}  # Dictionary

print(type(name))
print(type(age))
print(type(height))
print(type(is_student))
print(type(my_list))
print(type(my_tuple))
print(type(my_dict))


