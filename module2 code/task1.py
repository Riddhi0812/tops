# 1. Accessing list element
my_list = [10, "hello", 3.14, True, [1, 2, 3], {"a": 1, "b": 2}]
print(my_list)


print(my_list[0])  # Accessing the first element (integer)
print(my_list[1])  # Accessing the second element (string)
print(my_list[4]) # Accessing the nested list
print(my_list[-1]) # Accessing the last element (dictionary)

print(len(my_list))
