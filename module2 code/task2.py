#2. List Operations
my_list = [10, "Hello", 3.14, True, [1, 2, 3]]
my_list.append("New Element")
my_list.insert(2, "Inserted Element")
my_list.pop()  # Removes last element
my_list.pop(1)  # Removes element at index 1
my_list.remove(10)  # Removes first occurrence of value 10
print(my_list)


